<h3 class="project_title">Galleria - Photo Album Bowser</h3>

<p>	Lately, I've been teaching myself <a href="http://www.php.net/">PHP</a> (Php: Hypertext Preprocessing).
	This site is constructed from PHP scripts.</p>

<p>	My first PHP project was prompted by the increasing number of photo album's I've generated recently and the trouble I have browsing them all.
	A simple script, tentatively named <a href="galleria.php.zip">gallerie.php</a> scans a given web folder looking for sub-folders which contain photo albums that were created with <a href="http://gthumb.sourceforge.net/">gThumb</a>.
	For each of these albums, it displays a random thumbnail and a link to the album.</p>
	
<p>	You can see it in action on <a href="../gallery">my gallery page</a>. 
	(Hit 'reload' in your browser to see different random thumbnails.)</p>
