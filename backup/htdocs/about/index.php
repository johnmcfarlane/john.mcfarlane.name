<h1>About Me</h1>

<h2>Early Years</h2>

<p>I was born in England in 1973 and grew up near the village of <font class="proper">Shirenewton</font> in <font class="proper">South East Wales</font>.</p>

<p>My first computer arrived in 1984 and was a <font class="proper">BBC Micro B</font>. 
Very soon, I was teaching myself <font class="proper">BASIC</font> and <font class="proper">8086 assembler</font> from books and magazines and was greatly inspired by computer games such as <font class="proper">Elite</font> and <font class="proper">Repton</font>.</p>

<p>By the time I left school, I was writing in <font class="proper">C</font> and <font class="proper">80286 assembler</font> on a deafeningly loud Amstrad PC with a 28K modem and a SoundBlaster card. 
(Sadly, most of the noise was not coming from the SoundBlaster.)</p>

<h2>University</h2>

<p>I went on to study <font class="proper">Artificial Intelligence with Psychology</font> at <font class="proper">Middlesex University</font> in North London. 
During this time, I was lucky enough to find a work placement at <font class="proper">Intelligent Research Ltd.</font>, a company specialising in bespoke AI products. 
They furnished me with up-to-date PC hardware with which to write NLP-related programs (and play <font class="proper">Doom</font>).</p>

<p>After graduating, I moved to Brighton to gain a Master's Degree in <font class="proper">Evolutionary and Adaptive Systems</font> from the <font class="proper">University of Sussex</font>. 
While applying for a scholarship from a games company in Cambridge, I attended an interview with a senior developer of an artificial life-based title. 
During this interview, it dawned on me that I knew more about games than my interviewer. 
I decided there and then to get a job as a games programmer.</p>

<h2>Work</h2>

<p>Brighton was such a fun place to live that I decided to stick around and worked at various games companies in the area. 
For a few years, I was torn away from Brighton to seek work in Guildford which is where I met my future wife, Heather, who was studying in at <font class="proper">UCL</font> at the time.
I moved back to Brighton with her in the spring of 2006 and in the Autumn, she returned to the USA and her home state of Arizona. </p>

<h2>Emigration</h2>

<p>This year, I have followed Heather across the pond and in May, we were wed in <font class="proper">Las Vegas</font>. 
Our first child is due in early October and I am currently in the process of becomming a permanent resident of the United States.
I am looking for work while awaiting my permit to work here.</p>

