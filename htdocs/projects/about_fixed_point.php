<h3 class="project_title">fixed_point - C++ Fixed-point Arithmetic Library</h3>

<p>
This project stemmed from a desire to more accurately control the range and precision of variables used in simulation.
It quickly came to consume all spare dev time and become part of an effort to standardize fixed-point arithmetic in the C++ standard library.
It consists of a <a href="https://github.com/johnmcfarlane/fixed_point">reference implementation</a> and two proposal papers,
<a href="http://www.open-std.org/jtc1/sc22/wg21/docs/papers/2016/p0037r2.html">P0037</a>
and <a href="http://www.open-std.org/jtc1/sc22/wg21/docs/papers/2016/p0381r0.html">P0381</a>.
</p>
