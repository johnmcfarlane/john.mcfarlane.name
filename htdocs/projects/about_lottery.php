<h3 class="project_title">Lottery Number Generator - CA Route to Riches</h3>
<p>	Callular Automata are simple systems which produce complex results.
	Because of this, they make pretty good random number generators. 
	I've written a C.A. in C++ which is designed to produce numbers for the Arizona lottery.</p>
<p>	I've also written a <a href="lottery">description</a> of how it works and why I even bothered.</p>
