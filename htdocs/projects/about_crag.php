<h3 class="project_title">Crag - Real-time Fractal Landscape Generator</h3>

<p>This is a long-term project to simulate planetary bodies down to fine detail in real-time.
It's also a place for me to experiment with AI, C++, OpenGL and development skills in general.</p>

<img src="crag.png" alt="screen shot from the crag project showing a planet partially obscured by a moon" width="640" height="360" />

<p>Crag is hosted on <a href="http://code.google.com/p/crag/">Google Code</a> and includes downloads for Android and Windows as well as instructions for building on Linux and Raspberry Pi.</p>
