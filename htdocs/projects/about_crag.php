<h3 class="project_title">Crag - Real-time Fractal Landscape Generator</h3>

<p>This is a long-term project to simulate planetary bodies down to fine detail in real-time.
It's also a place for me to experiment with AI, C++, OpenGL and development skills in general.</p>

<img src="http://crag.googlecode.com/files/crag_screen_3.jpg" alt="screen shot from the crag project showing a planet partially obscured by a moon" />

<p>Crag is hosted on <a href="http://code.google.com/p/crag/">Google Code</a>. You can find Android, Linux, OS X and Windows versions of the simulation on the <a href="http://code.google.com/p/crag/wiki/Downloads">downloads</a> page.</p>

