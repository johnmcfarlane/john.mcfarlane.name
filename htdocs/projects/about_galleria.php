<h3 class="project_title">Galleria - Photo Album Bowser</h3>

<p>	I greatly enjoyed teaching myself <a href="http://www.php.net/">PHP</a> (Php: Hypertext Preprocessing).
	This site is constructed from PHP scripts.</p>

<p>	My first PHP project was prompted by the increasing number of photo albums I generated after I moved abroad. 
	A simple script, tentatively named <a href="galleria.php.zip">galleria.php</a>, scans a given web folder looking for sub-folders which contain photo albums that were created with <a href="http://gthumb.sourceforge.net/">gThumb</a>.
	For each of these albums, it displays a random thumbnail and a link to the album.</p>
	
<p>	You can see it in action on <a href="../gallery/2007/">my gallery page</a>. 
	(Hit 'reload' in your browser to see different random thumbnails.)</p>
