<h3 class="project_title">Plasma - Android Eye Candy</h3>

<p>Over the 2010 Christmas break, I decided to dabble in development for the <a href="http://www.android.com/">Android</a> mobile phone platform. 
The result is <a href="http://code.google.com/p/crag/downloads/detail?name=Ming.apk">a quirky app</a> that draws pretty colours on the screen. 
You'll need Android 2.2 to run it. (You also need to change the option in your setting to allow non-market apps to run.)</p>
